#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	storage;

	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		n = 147483648;
	}
	if (n < 0)
	{
		n *= -1;
		ft_putchar_fd('-', fd);
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	storage = (n % 10) + 48;
	ft_putchar_fd(storage, fd);
}
